use crate::ast::{ArgType, Fragment, ResultType};
use crate::codegen::python::PythonCodeGenerator;
use crate::NamedDocument;

use std::io;
use std::os::linux::raw::stat;

const PREAMBLE: &str = r#"
from __future__ import annotations

import contextlib

from typing import Any, Iterator, NamedTuple, Optional

import psycopg2.extensions  # type: ignore
import psycopg2.extras  # type: ignore
import psycopg2.pool  # type: ignore


class Transaction:
    def __init__(self, conn: psycopg2.extensions.connection) -> None:
        self.conn = conn

    def commit(self) -> None:
        self.conn.commit()
        # Ensure we cannot reuse the connection.
        self.conn = None

    def rollback(self) -> None:
        self.conn.rollback()
        self.conn = None

    def cursor(self) -> psycopg2.extensions.cursor:
        return self.conn.cursor()


class ConnectionPool(NamedTuple):
    pool: psycopg2.pool.ThreadedConnectionPool

    @contextlib.contextmanager
    def begin(self) -> Iterator[Transaction]:
        conn: Optional[psycopg2.extensions.connection] = None
        try:
            # Use psycopg2 in "no-autocommit" mode, where it implicitly starts a
            # transaction at the first statement, and we need to explicitly
            # commit() or rollback() afterwards.
            conn = self.pool.getconn()
            conn.isolation_level = "SERIALIZABLE"
            conn.autocommit = False
            yield Transaction(conn)

        except:
            if conn is not None:
                self.pool.putconn(conn, close=True)
            raise

        else:
            assert conn is not None
            self.pool.putconn(conn, close=False)
"#;

/// Generate Python code that uses the `psycopg2` package.
pub fn process_documents(out: &mut dyn io::Write, documents: &[NamedDocument]) -> io::Result<()> {
    use crate::version::{REV, VERSION};

    let mut gen = PythonCodeGenerator::new(out);

    write!(gen, "# This file was generated by Squiller {} ", VERSION)?;
    match REV {
        Some(rev) => writeln!(gen, " (commit {}).", &rev[..10])?,
        None => writeln!(gen, " (unspecified checkout).")?,
    }
    writeln!(gen, "# Input files:")?;
    for doc in documents {
        writeln!(gen, "# - {}", doc.fname.to_string_lossy())?;
    }

    gen.write(PREAMBLE)?;

    for named_document in documents {
        let input = named_document.input;

        for query in named_document.document.iter_queries() {
            let ann = &query.annotation;

            write!(gen, "\n\ndef {}(tx: Transaction", ann.name.resolve(input))?;

            match &ann.arguments {
                ArgType::Args(args) => {
                    for arg in args {
                        // TODO: Include types.
                        gen.write(", ")?;
                        gen.write(arg.ident.resolve(input))?;
                    }
                }
                ArgType::Struct {
                    var_name,
                    type_name,
                    ..
                } => {
                    write!(
                        gen,
                        ", {}: {}",
                        var_name.resolve(input),
                        type_name.resolve(input)
                    )?;
                }
            }

            gen.write(") -> ")?;
            match &ann.result_type {
                ResultType::Unit => gen.write("None:\n")?,
                ResultType::Option(_t) => {
                    // TODO: Write the actual type.
                    // TODO: Ensure import.
                    gen.write("Optional[Any]:\n")?;
                }
                ResultType::Single(_t) => {
                    // TODO: Write the actual type.
                    gen.write("Any:\n")?;
                }
                ResultType::Iterator(_t) => {
                    // TODO: Write the actual type.
                    // TODO: Ensure import.
                    gen.write("Iterator[Any]:\n")?;
                }
            }

            gen.open_scope();

            // Write the docstring, if there are doc comments.
            if !query.docs.is_empty() {
                gen.write_indent()?;
                gen.write("\"\"\"\n")?;
                for doc_line in &query.docs {
                    gen.write_indent()?;
                    // The comment lines usually start with a space that went after
                    // the "--" that starts the comment. In Python docstrings, we
                    // don't want to start the line with a space, so remove them.
                    let doc_line_str = doc_line.resolve(input);
                    let line_content = match doc_line_str.as_bytes().first() {
                        Some(b' ') => &doc_line_str[1..],
                        _ => doc_line_str,
                    };
                    gen.write(line_content)?;
                    gen.write("\n")?;
                }
                gen.write_indent()?;
                gen.write("\"\"\"\n")?;
            }

            for (i, statement) in query.statements.iter().enumerate() {
                gen.write_indent()?;
                gen.write("sql = \n")?;
                gen.increase_indent();
                gen.write_indent()?;
                gen.write("\"\"\"\n")?;
                gen.write_indent()?;

                let fragments = &statement.fragments;
                // TODO: Include the source file name and line number as a comment.
                for fragment in fragments {
                    let span = match fragment {
                        Fragment::Verbatim(span) => span.resolve(input),
                        Fragment::Param(_span) => "%s",
                        // When we put the SQL in the source code, omit the type
                        // annotations, it's only a distraction.
                        Fragment::TypedIdent(_full_span, ti) => ti.ident.resolve(input),
                        Fragment::TypedParam(_full_span, _ti) => "%s",
                    };
                    let mut lines = span.lines();
                    let first_line = lines.next().expect("Fragment should not be empty.");
                    gen.write(first_line)?;

                    for next_line in lines {
                        gen.write("\n")?;
                        gen.write_indent()?;
                        gen.write(next_line)?;
                    }
                }
                gen.write("\n")?;
                gen.write_indent()?;
                gen.write("\"\"\"\n")?;
                gen.decrease_indent();

                if statement.iter_parameters().next().is_some() {
                    // Write the parameter tuple. We used the counted %s-style
                    // references rather than the named ones (to save a dict lookup),
                    // so we just write out the references in the same order, if the
                    // same parameter is referenced twice, it occurs twice in the tuple.
                    gen.write_indent()?;
                    gen.write("params = (\n")?;
                    gen.increase_indent();

                    for param in statement.iter_parameters() {
                        // Cut off the leading ':' from the parameter name.
                        let variable_name = param.trim_start(1).resolve(input);
                        gen.write_indent()?;
                        // TODO: Deal with prefix in case we are accessing a struct.
                        gen.write(variable_name)?;
                        gen.write(",\n")?;
                    }

                    gen.decrease_indent();
                    gen.write_indent()?;
                    gen.write(")\n")?;
                } else {
                    gen.write_indent()?;
                    gen.write("params = (,)\n")?;
                }
            }

            gen.write_indent()?;
            gen.write("return None\n")?;
            gen.close_scope();
        }
    }

    Ok(())
}
